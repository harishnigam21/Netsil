$(document).ready(function() {
        $('.btn-main5').bind("mouseover", function(){
            var color  = $(this).css("background-color");

            $(this).css("background", "black");

            $(this).bind("mouseout", function(){
                $(this).css("background", color);
            })    
        })    
    })

